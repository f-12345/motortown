function money(n) {
  if (n === null || n === undefined) return "Call for Price";
  return n.toLocaleString("en-US", { style: "currency", currency: "USD", maximumFractionDigits: 0 });
}

// Lightbox state
let lbCarIdx = -1;
let lbPhotoIdx = 0;

function openLightbox(carIdx, photoIdx) {
  lbCarIdx = carIdx;
  lbPhotoIdx = photoIdx || 0;
  updateLightbox();
  const lb = document.getElementById("lightbox");
  lb.classList.add("open");
  lb.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeLightbox() {
  const lb = document.getElementById("lightbox");
  lb.classList.remove("open");
  lb.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
}

function updateLightbox() {
  const car = cars[lbCarIdx];
  if (!car) return;
  const photos = car.photos && car.photos.length ? car.photos : [car.img];
  const imgEl = document.getElementById("lbImg");
  const counterEl = document.getElementById("lbCounter");
  const titleEl = document.getElementById("lbTitle");
  const prevBtn = document.getElementById("lbPrev");
  const nextBtn = document.getElementById("lbNext");

  imgEl.src = photos[lbPhotoIdx];
  imgEl.alt = car.title;
  if (titleEl) titleEl.textContent = car.title;
  const multi = photos.length > 1;
  if (counterEl) counterEl.textContent = multi ? `${lbPhotoIdx + 1} / ${photos.length}` : "";
  if (prevBtn) prevBtn.style.display = multi ? "flex" : "none";
  if (nextBtn) nextBtn.style.display = multi ? "flex" : "none";
}

function lbNext() {
  const car = cars[lbCarIdx] || {};
  const photos = (car.photos && car.photos.length) ? car.photos : [car.img];
  if (!photos.length) return;
  lbPhotoIdx = (lbPhotoIdx + 1) % photos.length;
  updateLightbox();
}

function lbPrev() {
  const car = cars[lbCarIdx] || {};
  const photos = (car.photos && car.photos.length) ? car.photos : [car.img];
  if (!photos.length) return;
  lbPhotoIdx = (lbPhotoIdx - 1 + photos.length) % photos.length;
  updateLightbox();
}

function render(list) {
  const grid = document.querySelector("#invGrid");
  if (!grid) return;

  if (list.length === 0) {
    grid.innerHTML = `<div class="card" style="grid-column:1/-1;"><h3>No vehicles match your search.</h3><p>Try adjusting your filters.</p></div>`;
    return;
  }

  grid.innerHTML = list.map((c) => {
    const carIdx = cars.indexOf(c);
    const photos = (c.photos && c.photos.length) ? c.photos : [c.img];
    const multi = photos.length > 1;

    const details = [
      c.color      ? `<div class="inv-line"><span class="inv-k">Color</span><span class="inv-v">${c.color}</span></div>` : "",
      c.engine     ? `<div class="inv-line"><span class="inv-k">Engine</span><span class="inv-v">${c.engine}</span></div>` : "",
      c.drivetrain ? `<div class="inv-line"><span class="inv-k">Drivetrain</span><span class="inv-v">${c.drivetrain}</span></div>` : "",
      c.vin        ? `<div class="inv-line"><span class="inv-k">VIN</span><span class="inv-v inv-vin">${c.vin}</span></div>` : "",
    ].filter(Boolean).join("");

    const imgSection = c.img ? `
        <div class="inv-img-wrap" onclick="openLightbox(${carIdx}, 0)">
          <img class="inv-img" src="${c.img}" alt="${c.title}">
          <div class="img-hover-overlay"><span>View Photos</span></div>
          ${multi ? `<div class="photo-count-badge">${photos.length} photos</div>` : ""}
        </div>` : "";

    return `
      <div class="inv-card">
        ${imgSection}
        <div class="inv-body">
          <h3 class="inv-title">${c.title}</h3>
          <div class="inv-meta">${c.miles} miles &bull; ${c.trans}</div>
          <div class="inv-price">
            <div style="font-weight:800; font-size:18px;">${money(c.price)}</div>
            <div class="badge">${c.tag}</div>
          </div>
          <div class="inv-details">${details}</div>
          <div class="btn-row" style="margin-top:10px;">
            <a class="btn primary" href="contact.html">Check Availability</a>
            <a class="btn" href="get-prequalified.html">Get Pre-Qualified</a>
          </div>
        </div>
      </div>
    `;
  }).join("");
}

function applyFilters() {
  const q = (document.querySelector("#q")?.value || "").toLowerCase();
  const max = Number(document.querySelector("#maxPrice")?.value || 999999);

  const filtered = cars.filter((c) => {
    const matchesText = c.title.toLowerCase().includes(q) ||
                        (c.color || "").toLowerCase().includes(q) ||
                        (c.tag || "").toLowerCase().includes(q);
    const matchesPrice = c.price === null || c.price <= max;
    return matchesText && matchesPrice;
  });

  render(filtered);
}

document.addEventListener("DOMContentLoaded", () => {
  render(cars);

  const q = document.querySelector("#q");
  const max = document.querySelector("#maxPrice");
  const maxLabel = document.querySelector("#maxLabel");

  if (max && maxLabel) {
    maxLabel.textContent = money(Number(max.value));
    max.addEventListener("input", () => {
      maxLabel.textContent = money(Number(max.value));
      applyFilters();
    });
  }

  if (q) q.addEventListener("input", applyFilters);

  const clearBtn = document.querySelector("#clearFilters");
  if (clearBtn) {
    clearBtn.addEventListener("click", () => {
      if (q) q.value = "";
      if (max) max.value = "200000";
      if (maxLabel) maxLabel.textContent = money(200000);
      render(cars);
    });
  }

  // Lightbox controls
  document.getElementById("lbClose")?.addEventListener("click", closeLightbox);
  document.getElementById("lbNext")?.addEventListener("click", lbNext);
  document.getElementById("lbPrev")?.addEventListener("click", lbPrev);
  document.getElementById("lbOverlay")?.addEventListener("click", closeLightbox);

  // Keyboard navigation
  document.addEventListener("keydown", (e) => {
    if (!document.getElementById("lightbox")?.classList.contains("open")) return;
    if (e.key === "Escape") closeLightbox();
    if (e.key === "ArrowRight") lbNext();
    if (e.key === "ArrowLeft") lbPrev();
  });

  // Touch swipe
  let touchStartX = 0;
  const lbImg = document.getElementById("lbImg");
  if (lbImg) {
    lbImg.addEventListener("touchstart", (e) => { touchStartX = e.touches[0].clientX; }, { passive: true });
    lbImg.addEventListener("touchend", (e) => {
      const dx = e.changedTouches[0].clientX - touchStartX;
      if (Math.abs(dx) > 50) dx < 0 ? lbNext() : lbPrev();
    });
  }
});
